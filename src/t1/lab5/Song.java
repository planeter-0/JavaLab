package t1.lab5;

public class Song {
    public static void main(String[] args) {
        int num = 10;
        while (num > 1) {
            System.out.println(num + " in a bed and the little one said,\n" +
                    "'Roll over, roll over'\n" +
                    "They all rolled over and one fell out,");
            num--;
        }
        System.out.println("1 in a bed and the little one said,\n" +"'Alone at last'");
    }
}
